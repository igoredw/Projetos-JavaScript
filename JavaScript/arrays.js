// Arrays é uma variável que contém um conjunto de dados usando [] e separando por vírgulas

let listaConvidados = [
    "Ana",
    "Marcos",
    "Maria",
    "João"
];
console.log(listaConvidados);

// Inserir valores no final da arrays utilizando a função push()

let listaConvida = []
listaConvida.push("Amanda")
listaConvida.push("Manoel", "Suelen", "Carol");
console.log(listaConvida);

// Método pop() utilizado para remover o último valor do arrays. Além de remover ele guarda o valor para si.

let listaConvidads = ["Ana", "Marcos", "Maria", "João"]
listaConvidads.pop();
console.log(listaConvidads);

